# Assigning a single value to multiple variables
x = y = z = 42
print("Single value assigned to multiple variables:")
print("x:", x)
print("y:", y)
print("z:", z)

# Assigning multiple values to multiple variables
a, b, c = 1, 2, 3
print("\nMultiple values assigned to multiple variables:")
print("a:", a)
print("b:", b)
print("c:", c)